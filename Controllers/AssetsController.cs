        /// <summary>
        /// DOWNLOAD QR CODE - Downloads the QR code as a PNG file
        /// </summary>
        [Authorize]
        public async Task<IActionResult> DownloadQRCode(int id)
        {
            var asset = await _context.Assets.FindAsync(id);
            if (asset == null)
                return NotFound();

            // Generate the URL that the QR code will point to
            var baseUrl = $"{Request.Scheme}://{Request.Host}";
            var qrUrl = asset.GetQRCodeUrl(baseUrl);

            // Generate QR code as byte array with higher resolution for printing
            byte[] qrCodeBytes = QRCodeHelper.GenerateQRCodeBytes(qrUrl, 20);

            // Clean the asset tag for use in filename
            var safeAssetTag = (asset.AssetTag ?? "Unknown").Replace(" ", "_").Replace("/", "-");
            var fileName = $"QR_{safeAssetTag}.png";

            return File(qrCodeBytes, "image/png", fileName);
        }

