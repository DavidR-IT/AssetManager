        /// <summary>
        /// RETURN MULTIPLE ASSETS - Issuer Only (POST)
        /// Process the return of multiple assets
        /// </summary>
        [HttpPost]
        [Authorize(Roles = "Issuer")]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> ReturnMultipleAssetsConfirmed(List<int> assetIds, string? returnNotes)
        {
            if (assetIds == null || !assetIds.Any())
            {
                TempData["Error"] = "Please select at least one asset to return.";
                return RedirectToAction(nameof(ReturnMultipleAssets));
            }

            // Get current user info
            var currentUserId = int.TryParse(User.FindFirstValue(ClaimTypes.NameIdentifier), out var uid) ? uid : 0;
            var currentUserEmail = User.FindFirstValue(ClaimTypes.Email);

            // Load the assets
            var assets = await _context.Assets
                .Where(a => assetIds.Contains(a.Id))
                .ToListAsync();

            if (assets.Count != assetIds.Count)
            {
                TempData["Error"] = "One or more selected assets could not be found.";
                return RedirectToAction(nameof(ReturnMultipleAssets));
            }

            // Verify all are assigned
            var notAssignedAssets = assets.Where(a => a.Status != AssetStatus.Assigned).ToList();
            if (notAssignedAssets.Any())
            {
                TempData["Error"] = $"The following assets are not assigned: {string.Join(", ", notAssignedAssets.Select(a => a.AssetTag))}";
                return RedirectToAction(nameof(ReturnMultipleAssets));
            }

            using var transaction = await _context.Database.BeginTransactionAsync();
            try
            {
                // Create a single return request for all assets
                var returnRequest = new AssetRequest
                {
                    UserId = currentUserId,
                    RequestedBy = currentUserEmail ?? "Unknown",
                    RequestType = RequestTypes.Return,
                    Status = RequestStatuses.Approved, // Auto-approve returns
                    RequestDate = DateTime.UtcNow,
                    ApprovalDate = DateTime.UtcNow,
                    ApprovedBy = currentUserEmail ?? "Unknown",
                    Notes = $"Bulk return of {assets.Count} asset(s) by {currentUserEmail ?? "Unknown"}" +
                            (string.IsNullOrWhiteSpace(returnNotes) ? "" : $"\nNotes: {returnNotes}")
                };

                _context.AssetRequests.Add(returnRequest);
                await _context.SaveChangesAsync(); // Save to get the request ID

                // Create request items and update each asset
                var assigneesList = new List<string>();
                foreach (var asset in assets)
                {
                    var requestItem = new AssetRequestItem
                    {
                        AssetRequestId = returnRequest.Id,
                        AssetId = asset.Id
                    };
                    _context.AssetRequestItems.Add(requestItem);

                    // Track who had the asset
                    if (!string.IsNullOrEmpty(asset.AssignedTo) && !assigneesList.Contains(asset.AssignedTo))
                    {
                        assigneesList.Add(asset.AssignedTo);
                    }

                    // Update asset status
                    asset.Status = AssetStatus.Ready;
                    asset.AssignedTo = null;
                    asset.AssignedDate = null;
                    asset.UserId = null;
                    _context.Assets.Update(asset);
                }

                await _context.SaveChangesAsync();
                await transaction.CommitAsync();

                var assigneesText = assigneesList.Any()
                    ? $" from {string.Join(", ", assigneesList)}"
                    : "";

                TempData["Success"] = $"Successfully returned {assets.Count} asset(s){assigneesText}. All assets are now available.";
                return RedirectToAction(nameof(Index));
            }
            catch (Exception ex)
            {
                await transaction.RollbackAsync();
                _logger.LogError(ex, "Failed to bulk return assets for user {UserId}", currentUserId);
                TempData["Error"] = "Failed to return assets. Please try again or contact support.";
                return RedirectToAction(nameof(ReturnMultipleAssets));
            }
        }

        /// <summary>
        /// GENERATE QR CODE - Generates QR code image for an asset
        /// Returns a PNG image of the QR code
        /// </summary>
        [Authorize]
        public async Task<IActionResult> GenerateQRCode(int id)
        {
            var asset = await _context.Assets.FindAsync(id);
            if (asset == null)
                return NotFound();

            // Generate the URL that the QR code will point to
            var baseUrl = $"{Request.Scheme}://{Request.Host}";
            var qrUrl = asset.GetQRCodeUrl(baseUrl);

            // Generate QR code as byte array
            byte[] qrCodeBytes = QRCodeHelper.GenerateQRCodeBytes(qrUrl, 10);

            return File(qrCodeBytes, "image/png");
        }

