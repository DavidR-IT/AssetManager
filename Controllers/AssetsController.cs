        /// <summary>
        /// VIEW MY REQUESTS - Shows user's own asset requests
        /// </summary>
        public async Task<IActionResult> MyRequests()
        {
            var userEmail = User.FindFirstValue(ClaimTypes.Email) ?? string.Empty;
            var userId = int.TryParse(User.FindFirstValue(ClaimTypes.NameIdentifier), out var uid) ? uid : 0;

            var requests = await _context.AssetRequests
                .Include(r => r.Asset)
                .Include(r => r.RequestedAssets)
                    .ThenInclude(ri => ri.Asset)
                .Where(r => r.UserId == userId || r.RequestedBy == userEmail)
                .OrderByDescending(r => r.RequestDate)
                .ToListAsync();

            return View(requests);
        }

