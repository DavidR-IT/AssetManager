        [HttpPost]
        [Authorize(Roles = "Admin")]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Create([Bind("AssetTag,Category,Brand,Model,SerialNumber,PurchaseDate,PurchasePrice,Location,Notes")] Asset asset)
        {
            // RowVersion is auto-generated by the database on insert — it is never posted from the form.
            // Remove it from ModelState so its absence does not silently block the Create action.
            ModelState.Remove(nameof(Asset.RowVersion));

            if (!ModelState.IsValid)
                return View(asset);

            asset.Status = AssetStatus.Ready;
            asset.AssignedDate = null;
            asset.AssignedTo = null;
            asset.UserId = null;

            _context.Assets.Add(asset);
            await _context.SaveChangesAsync();

            TempData["Success"] = "Asset created successfully!";
            return RedirectToAction(nameof(Index));
        }

